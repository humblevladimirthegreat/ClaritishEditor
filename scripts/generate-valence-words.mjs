const isWordLike = (word) => /^[a-z][a-z'-]*$/i.test(word);

function escapeRegex(word) {
  return word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function buildAlternation(words) {
  return words.map(escapeRegex).join('|');
}

function wordsFromAfinn(scorePredicate) {
  return Object.entries(afinn165)
    .filter(([word, score]) => scorePredicate(score) && isWordLike(word))
    .map(([word]) => word.toLowerCase());
}

function mergeWords(afinnWords, extras) {
  return [...new Set([...afinnWords, ...extras.map((w) => w.toLowerCase())])]
    .filter(isWordLike)
    .sort((a, b) => a.localeCompare(b));
}

function toRegexConst(name, words) {
  return `const ${name} = "(?:${buildAlternation(words)})";`;
}

const BEGIN = '// BEGIN GENERATED VALENCE WORDS';
const END = '// END GENERATED VALENCE WORDS';

const positiveWords = mergeWords(
  wordsFromAfinn((score) => score >= THRESHOLD),
  EXTRA_POSITIVE,
);
const negativeWords = mergeWords(
  wordsFromAfinn((score) => score <= -THRESHOLD),
  EXTRA_NEGATIVE,
);

const generatedBlock = [
  '// Generated by scripts/generate-valence-words.mjs — do not edit by hand.',
  BEGIN,
  toRegexConst('POSITIVE_WORDS', positiveWords),
  toRegexConst('NEGATIVE_WORDS', negativeWords),
  END,
].join('\n');

fs.writeFileSync(VALENCE_WORDS_PATH, `${generatedBlock}\n`);

// Remove legacy inline block from rules.js if present (one-time migration).
let rules = fs.readFileSync(RULES_PATH, 'utf8');
const blockRegex = new RegExp(
  `\\n?${BEGIN}[\\s\\S]*?${END}\\n?`,
  'm',
);
if (blockRegex.test(rules)) {
  rules = rules.replace(blockRegex, '\n');
  fs.writeFileSync(RULES_PATH, rules);
}

console.log(
  `Updated valence-words.js: ${positiveWords.length} positive, ${negativeWords.length} negative words (AFINN-165 threshold ±${THRESHOLD} + extras).`,
);
